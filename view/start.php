    <h4>Der Arcwind Datastore</h4>


    <p>
        Arcwind ist ein Datenspeicherdienst der Zeitserien speichert ohne weitere Annahmen über die Daten zu
        treffen. Damit kann Arcwind vor allem als Backend für verschiedene IoT Gadgets verwendet werden, die Ihre
        Sensordaten zentral im Internet speichern sollen.
        <br/>
        Alle Daten können per RESTfull API gespeichert und wieder ausgelesen werden.
        <br/>
        Über das Arcwind Webinterface können dabei die Daten direkt visualisiert werden und Sensoren Live verfolgt
        werden.
    </p>

    <h4>Usage Aggreement</h4>

    <p>
        Arcwind ist aktuell eine <b>ALPHA</b> Version. Das bedeutet einerseits, dass es zur Zeit komplett kostenlos
        ist für jeden der es benutzen möchte. Andererseits kann es allerdings auch jederzeit kaputt gehen und es
        wird keine Funktionalität welcher Art auch immer garantiert!


